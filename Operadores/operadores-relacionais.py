"""
Operadores relacionais
"""
x = 5;
y = 7;

#Maior que
print(x > y); #False
print()
#Menor que
print(x < y) #True
print();
#Igualdade
print(x == y,"\n") #False

#Maior ou igual a
print(x >= y); #False
print()
#Menor ou igual a
print(x <= y); #True
print()
#Diferente de
print(x != y); #True
print();

